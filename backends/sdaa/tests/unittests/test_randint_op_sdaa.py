def randint_wapper(
    low,
    high,
    shape,
    dtype=None,
):
    if dtype is None:
        dtype = paddle.int64

    return _C_ops.randint(low, high, shape, dtype, _current_expected_place())


def output_hist(out):
    hist, _ = np.histogram(out, range=(-10, 10))
    hist = hist.astype("float32")
    hist /= float(out.size)
    prob = 0.1 * np.ones(10)
    return hist, prob


class TestSDAARandIntOp(OpTest):
    def setUp(self):
        self.op_type = "randint"
        self.init_dtype()
        self.init_shape()
        self.set_sdaa()
        self.python_api = randint_wapper
        self.set_attrs()

        self.atol = 1e-4
        self.inputs = {}
        self.outputs = {"Out": np.zeros(self.shape).astype("float32")}

        self.output_hist = output_hist

    def set_attrs(self):
        self.attrs = {
            "shape": self.shape,
            "low": -10,
            "high": 10,
            "seed": SEED,
        }

    def init_shape(self):
        self.shape = [1000, 784]

    def init_dtype(self):
        self.dtype = np.int64

    def set_sdaa(self):
        self.__class__.use_custom_device = True
        self.place = paddle.CustomPlace("sdaa", 0)

    def test_check_output(self):
        self.check_output_customized(self.verify_output, self.place)

    def verify_output(self, outs):
        hist, prob = self.output_hist(np.array(outs[0]))
        np.testing.assert_allclose(hist, prob, rtol=0, atol=0.001)


